public class MyMath {

    public static double Plus(double a, double b)
    {
        return a + b;
    }

    public static double Minus(double a, double b)
    {
        return a - b;
    }

    public static double Multi(double a, double b)
    {
        return a * b;
    }

    public static double Divide(double a, double b)
    {
        return a / b;
    }
}
